import UIKit

extension UIColor {
    class func customGreen() -> UIColor {
        return UIColor(red: 0/255, green: 38/255, blue: 26/255, alpha: 1)
    }
}

extension UIColor {
    class func customGreen2() -> UIColor {
        return UIColor(red: 0/255, green: 90/255, blue: 63/255, alpha: 1)
    }
}

extension UIColor {
    class func customYellow() -> UIColor {
        return UIColor(red: 201/255, green: 213/255, blue: 51/255, alpha: 1)
    }
}
